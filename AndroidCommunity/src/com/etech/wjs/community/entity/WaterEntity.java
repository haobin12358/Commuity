package com.etech.wjs.community.entity;

public class WaterEntity {
	
	private String Wid;
	private int Wyear;
	private int Wmonth;
	private double Wpay;
	private String Wstatus;
	public String getWid() {
		return Wid;
	}
	public void setWid(String wid) {
		Wid = wid;
	}
	public int getWyear() {
		return Wyear;
	}
	public void setWyear(int wyear) {
		Wyear = wyear;
	}
	public int getWmonth() {
		return Wmonth;
	}
	public void setWmonth(int wmonth) {
		Wmonth = wmonth;
	}
	public double getWpay() {
		return Wpay;
	}
	public void setWpay(double wpay) {
		Wpay = wpay;
	}
	public String getWstatus() {
		return Wstatus;
	}
	public void setWstatus(String wstatus) {
		Wstatus = wstatus;
	}
	

}
